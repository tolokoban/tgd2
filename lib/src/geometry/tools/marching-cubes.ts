import { TgdDataset } from "@tgd/dataset"
import { ArrayNumber3 } from "@tgd/types"
import { TgdGeometry } from "../geometry"

interface MakeGeometryFromVolumeOptions {
    bboxCorner: ArrayNumber3
    bboxSize: ArrayNumber3
    /**
     * Size of the side of the voxel we use to mesh the volume.
     */
    voxelSize: number
    /**
     * Signed distance function that defines the volume.
     * @returns A Negative number if `point` is inside the volume.
     */
    sdf(point: ArrayNumber3): number
    /**
     * By default the name of the attribute position is "POSITION".
     */
    attPosition?: string
}

export function tgdMakeGeometryFromVolume({
    bboxSize,
    voxelSize,
    attPosition = "POSITION",
}: MakeGeometryFromVolumeOptions): TgdGeometry {
    const dimX = Math.ceil(bboxSize[0] / voxelSize)
    const dimY = Math.ceil(bboxSize[1] / voxelSize)
    const dimZ = Math.ceil(bboxSize[2] / voxelSize)

    const dataset = new TgdDataset({
        [attPosition]: "vec3",
    })
    return new TgdGeometry({ dataset, attPosition })
}

